    private static async Task EnsureDocumentSchemaAsync(ServicePortalDbContext db, CancellationToken cancellationToken)
    {
        await db.Database.ExecuteSqlRawAsync(
            """
            CREATE TABLE IF NOT EXISTS document_categories (
                "Id" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                "NameAr" VARCHAR(255) NOT NULL,
                "NameEn" TEXT NULL,
                "Code" VARCHAR(100) NOT NULL,
                "Description" TEXT NULL,
                "Icon" VARCHAR(80) NULL,
                "Color" VARCHAR(30) NOT NULL DEFAULT '#0f5132',
                "SortOrder" INTEGER NOT NULL DEFAULT 100,
                "IsActive" BOOLEAN NOT NULL DEFAULT TRUE,
                "CreatedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW(),
                "UpdatedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW()
            );
            CREATE UNIQUE INDEX IF NOT EXISTS "IX_document_categories_Code" ON document_categories ("Code");

            CREATE TABLE IF NOT EXISTS documents (
                "Id" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                "CategoryId" BIGINT NOT NULL REFERENCES document_categories ("Id") ON DELETE RESTRICT,
                "TitleAr" VARCHAR(300) NOT NULL,
                "TitleEn" TEXT NULL,
                "DocumentNumber" VARCHAR(100) NULL,
                "Description" TEXT NULL,
                "OwnerDepartmentId" BIGINT NULL REFERENCES departments ("Id") ON DELETE SET NULL,
                "Classification" VARCHAR(40) NOT NULL DEFAULT 'internal',
                "Status" VARCHAR(40) NOT NULL DEFAULT 'active',
                "CurrentVersionId" BIGINT NULL,
                "RequiresAcknowledgement" BOOLEAN NOT NULL DEFAULT FALSE,
                "Keywords" TEXT NULL,
                "IsActive" BOOLEAN NOT NULL DEFAULT TRUE,
                "CreatedByUserId" BIGINT NOT NULL REFERENCES users ("Id") ON DELETE RESTRICT,
                "CreatedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW(),
                "UpdatedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW()
            );
            CREATE INDEX IF NOT EXISTS "IX_documents_CategoryId" ON documents ("CategoryId");
            CREATE INDEX IF NOT EXISTS "IX_documents_DocumentNumber" ON documents ("DocumentNumber");
            CREATE INDEX IF NOT EXISTS "IX_documents_Classification" ON documents ("Classification");
            CREATE INDEX IF NOT EXISTS "IX_documents_Status" ON documents ("Status");

            CREATE TABLE IF NOT EXISTS document_versions (
                "Id" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                "DocumentId" BIGINT NOT NULL REFERENCES documents ("Id") ON DELETE CASCADE,
                "VersionNumber" VARCHAR(40) NOT NULL,
                "FileName" VARCHAR(255) NOT NULL,
                "StoredFileName" VARCHAR(255) NOT NULL,
                "FilePath" TEXT NOT NULL,
                "FileSize" BIGINT NOT NULL,
                "MimeType" VARCHAR(150) NOT NULL DEFAULT 'application/pdf',
                "Checksum" TEXT NOT NULL,
                "IssueDate" DATE NULL,
                "EffectiveDate" DATE NULL,
                "ReviewDate" DATE NULL,
                "UploadedByUserId" BIGINT NOT NULL REFERENCES users ("Id") ON DELETE RESTRICT,
                "UploadedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW(),
                "ChangeSummary" TEXT NULL,
                "IsCurrent" BOOLEAN NOT NULL DEFAULT FALSE
            );
            CREATE UNIQUE INDEX IF NOT EXISTS "IX_document_versions_DocumentId_VersionNumber" ON document_versions ("DocumentId", "VersionNumber");
            DO $$
            BEGIN
                IF NOT EXISTS (
                    SELECT 1
                    FROM pg_constraint
                    WHERE conname = 'FK_documents_document_versions_CurrentVersionId'
                ) THEN
                    ALTER TABLE documents
                    ADD CONSTRAINT "FK_documents_document_versions_CurrentVersionId"
                    FOREIGN KEY ("CurrentVersionId") REFERENCES document_versions ("Id") ON DELETE SET NULL;
                END IF;
            END $$;

            CREATE TABLE IF NOT EXISTS document_permissions (
                "Id" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                "CategoryId" BIGINT NULL REFERENCES document_categories ("Id") ON DELETE CASCADE,
                "DocumentId" BIGINT NULL REFERENCES documents ("Id") ON DELETE CASCADE,
                "RoleId" BIGINT NULL REFERENCES roles ("Id") ON DELETE CASCADE,
                "DepartmentId" BIGINT NULL REFERENCES departments ("Id") ON DELETE CASCADE,
                "CanView" BOOLEAN NOT NULL DEFAULT TRUE,
                "CanDownload" BOOLEAN NOT NULL DEFAULT FALSE,
                "CanPrint" BOOLEAN NOT NULL DEFAULT FALSE,
                "CanManage" BOOLEAN NOT NULL DEFAULT FALSE,
                "CreatedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW(),
                "UpdatedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW()
            );
            CREATE INDEX IF NOT EXISTS "IX_document_permissions_CategoryId" ON document_permissions ("CategoryId");
            CREATE INDEX IF NOT EXISTS "IX_document_permissions_DocumentId" ON document_permissions ("DocumentId");

            CREATE TABLE IF NOT EXISTS document_access_logs (
                "Id" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                "DocumentId" BIGINT NOT NULL REFERENCES documents ("Id") ON DELETE CASCADE,
                "VersionId" BIGINT NULL REFERENCES document_versions ("Id") ON DELETE SET NULL,
                "UserId" BIGINT NOT NULL REFERENCES users ("Id") ON DELETE RESTRICT,
                "Action" VARCHAR(80) NOT NULL,
                "IpAddress" TEXT NULL,
                "UserAgent" TEXT NULL,
                "CreatedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW()
            );
            CREATE INDEX IF NOT EXISTS "IX_document_access_logs_DocumentId_CreatedAt" ON document_access_logs ("DocumentId", "CreatedAt");
            CREATE INDEX IF NOT EXISTS "IX_document_access_logs_Action" ON document_access_logs ("Action");

            CREATE TABLE IF NOT EXISTS document_acknowledgements (
                "Id" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                "DocumentId" BIGINT NOT NULL REFERENCES documents ("Id") ON DELETE CASCADE,
                "VersionId" BIGINT NOT NULL REFERENCES document_versions ("Id") ON DELETE CASCADE,
                "UserId" BIGINT NOT NULL REFERENCES users ("Id") ON DELETE CASCADE,
                "AcknowledgedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW()
            );
            CREATE UNIQUE INDEX IF NOT EXISTS "IX_document_acknowledgements_DocumentId_VersionId_UserId"
                ON document_acknowledgements ("DocumentId", "VersionId", "UserId");
            """,
            cancellationToken);
    }


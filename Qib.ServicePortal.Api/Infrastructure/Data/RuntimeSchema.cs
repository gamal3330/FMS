using Microsoft.EntityFrameworkCore;

namespace Qib.ServicePortal.Api.Infrastructure.Data;

public static class RuntimeSchema
{
    public static async Task EnsureAsync(ServicePortalDbContext db, ILogger logger, CancellationToken cancellationToken = default)
    {
        if (!db.Database.IsNpgsql())
        {
            return;
        }

        logger.LogInformation("ASP.NET backend startup: ensuring additive runtime schema");
        await EnsureUsersSchemaAsync(db, cancellationToken);
        await EnsureNotificationsSchemaAsync(db, cancellationToken);
        await EnsureMessagingSchemaAsync(db, cancellationToken);
        await EnsureDocumentSchemaAsync(db, cancellationToken);
        await EnsureOperationalSchemaAsync(db, cancellationToken);
    }

    private static async Task EnsureUsersSchemaAsync(ServicePortalDbContext db, CancellationToken cancellationToken)
    {
        await db.Database.ExecuteSqlRawAsync(
            """
            ALTER TABLE users ADD COLUMN IF NOT EXISTS "SpecializedSectionId" BIGINT NULL REFERENCES specialized_sections ("Id") ON DELETE SET NULL;
            ALTER TABLE users ADD COLUMN IF NOT EXISTS "RelationshipType" VARCHAR(80) NOT NULL DEFAULT 'employee';
            CREATE INDEX IF NOT EXISTS "IX_users_SpecializedSectionId" ON users ("SpecializedSectionId");
            """,
            cancellationToken);
    }

    private static async Task EnsureNotificationsSchemaAsync(ServicePortalDbContext db, CancellationToken cancellationToken)
    {
        await db.Database.ExecuteSqlRawAsync(
            """
            CREATE TABLE IF NOT EXISTS notifications (
                "Id" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                "UserId" BIGINT NOT NULL REFERENCES users ("Id") ON DELETE CASCADE,
                "Title" VARCHAR(255) NOT NULL,
                "Body" TEXT NOT NULL,
                "Channel" VARCHAR(80) NOT NULL DEFAULT 'system',
                "RelatedRoute" VARCHAR(300) NULL,
                "IsRead" BOOLEAN NOT NULL DEFAULT FALSE,
                "ReadAt" TIMESTAMPTZ NULL,
                "CreatedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW(),
                "UpdatedAt" TIMESTAMPTZ NOT NULL DEFAULT NOW()
            );
            CREATE INDEX IF NOT EXISTS "IX_notifications_UserId_IsRead_CreatedAt" ON notifications ("UserId", "IsRead", "CreatedAt");
            CREATE INDEX IF NOT EXISTS "IX_notifications_Channel" ON notifications ("Channel");
            ALTER TABLE notifications ADD COLUMN IF NOT EXISTS "RelatedRoute" VARCHAR(300) NULL;
            ALTER TABLE notifications ADD COLUMN IF NOT EXISTS "ReadAt" TIMESTAMPTZ NULL;
            """,
            cancellationToken);
    }

